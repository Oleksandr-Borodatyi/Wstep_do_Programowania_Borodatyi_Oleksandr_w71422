# Zdefiniowanie łańcucha tekstowego
tekst = "Rzeszów jest piękny"

# a) Wyświetlenie pierwszej litery w tekście
pierwsza_litera = tekst[0]
print("Pierwsza litera:", pierwsza_litera)

# b) Wyświetlenie siódmej, dziesiątej, trzynastej oraz drugiej litery
siodma_litera = tekst[6]  # siódma (indeks 6)
dziesiata_litera = tekst[9]  # dziesiąta (indeks 9)
trzynasta_litera = tekst[12]  # trzynasta (indeks 12)
druga_litera = tekst[1]  # druga (indeks 1)

# Wyświetlenie wyników
print("Siódma litera:", siodma_litera)
print("Dziesiąta litera:", dziesiata_litera)
print("Trzynasta litera:", trzynasta_litera)
print("Druga litera:", druga_litera)
